public class ExemploWhile {
    public static void main(String[] args) {

        int i = 0;

        while (i < 10) {
            System.out.println("Chines");

            i++;
        }

        System.out.println("--------------");

        int y = 6;

        do {
            System.out.println(y);

            y++;
        } while (y < 5);

    }
}
